package main

import "testing"

func TestHash(t *testing.T) {
    hash := hashAndEncode("angryMonkey")
    if hash != "ZEHhWB65gUlzdVwtDQArEyx+KVLzp/aTaRaPlBzYRIFj6vjFdqEb0Q5B8zVKCZ0vKbZPZklJz0Fd7su2A+gf7Q==" {
       t.Errorf("Hash was incorrect, got: %s, want: %s.", hash, "ZEHhWB65gUlzdVwtDQArEyx+KVLzp/aTaRaPlBzYRIFj6vjFdqEb0Q5B8zVKCZ0vKbZPZklJz0Fd7su2A+gf7Q==")
    }
}

